/**
 * UI Components Documentation
 *
 * Core Components:
 * - Button: Used for actions and navigation throughout the application
 * - Card: Container component for content sections with various subcomponents:
 *   - CardContent: Main content area
 *   - CardDescription: Descriptive text
 *   - CardFooter: Bottom section
 *   - CardHeader: Top section
 *   - CardTitle: Heading text
 *
 * Form Components:
 * - Form: Form wrapper with validation
 * - FormControl: Input wrapper
 * - FormDescription: Helper text
 * - FormField: Field container
 * - FormItem: Item wrapper
 * - FormLabel: Label text
 * - FormMessage: Error messages
 * - Input: Text input fields
 * - InputOTP: One-time password input
 * - Select: Dropdown selection
 * - Textarea: Multi-line text input
 *
 * Navigation Components:
 * - Tabs: Tabbed interface
 * - TabsContent: Tab content
 * - TabsList: Tab list container
 * - TabsTrigger: Tab trigger button
 *
 * Feedback Components:
 * - Alert: Status messages
 * - AlertDescription: Alert details
 * - AlertTitle: Alert heading
 * - Badge: Status indicators
 * - Progress: Progress indicators
 *
 * Media Components:
 * - Avatar: User profile images
 * - AvatarFallback: Fallback content
 * - AvatarImage: Image container
 *
 * Layout Components:
 * - Sheet: Side panel/drawer
 * - DropdownMenu: Context menus
 *
 * Note: Built on top of Radix UI primitives
 * and styled using Tailwind CSS for consistent design and accessibility,
 * and more UIs as necessary.
 */
